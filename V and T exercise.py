#__Variables and types exercise___#
A = "Minecraft"
B = 1.18
C = 599
if A == "Minecraft":
    print("String:",A)
if isinstance(B,float) and B == 1.18:
    print("Float:",B)
if isinstance(C,int) and C == 599:
    print("Integer:",C)
    
#Output
String: Minecraft
Float: 1.18
Integer: 599
