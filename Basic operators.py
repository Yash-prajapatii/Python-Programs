#Basic operators:
#addition
A = 113 + 137
print(A)

#subtraction
B = 267 - 133
print(B)

#multiplication
C = 25  * 17
print(C)

#division
D = 84 / 16
print(D)

#Combined all operators:
number = 190 + 140 - 34 * 5 / 2
print(number)

#modulo
E = 11914 % 5
print(E)

#Power
F = 5 ** 2
G = 4 ** 3
print(F)
print(G)

#Using operators with strings:
#adding strings
file = "Python" + " " + "Cpp" + " " + "Java"
print(file)

#multiplying strings
line = "Lotsoflove " * 5
print(line)

#Using operators with lists:
